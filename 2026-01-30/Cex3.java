public class Cex3 {
    public static void main(String[] args) {
        //8까지의 짝수 출력
        int i = 0;
        while (true) {
            i++;
            if (i==8){
                break;
            }
            if (i%2==0){
                System.out.println(i);
            }
        }
    }
}
